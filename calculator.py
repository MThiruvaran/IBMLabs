num1 = int(input("enter the number:"))
num2 = int(input("enter another number:"))

print('the sum of {0} and {1} is {2}'.format(num1, num2, num1 + num2))
print('the difference between {0} and {1} is {2}'.format(num1, num2, num1 - num2))
print('the product of {0} and {1} is {2}'.format(num1, num2, num1 * num2))
print('the quotient of {0} and {1} is {2}'.format(num1, num2, float(num1 / num2)))
print('the remainder of {0} and {1} is {2}'.format(num1, num2, float(num1 % num2)))